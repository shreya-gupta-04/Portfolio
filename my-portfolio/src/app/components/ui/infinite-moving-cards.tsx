"use client";
 
import { cn } from "@/lib/utils";
import React, { useEffect, useState } from "react";
 
export const InfiniteMovingCards = ({
  items,
  direction = "left",
  speed = "normal",
  pauseOnHover = true,
  className,
}: {
  items: {
    image: string; // Use image URL or icon path
    label: string;
  }[];
  direction?: "left" | "right";
  speed?: "superfast"|"fast" | "normal" | "slow";
  pauseOnHover?: boolean;
  className?: string;
}) => {
  const containerRef = React.useRef<HTMLDivElement>(null);
  const scrollerRef = React.useRef<HTMLUListElement>(null);
 
  useEffect(() => {
    addAnimation();
  }, []);
  const [start, setStart] = useState(false);
  function addAnimation() {
    if (containerRef.current && scrollerRef.current) {
      const scrollerContent = Array.from(scrollerRef.current.children);
 
      scrollerContent.forEach((item) => {
        const duplicatedItem = item.cloneNode(true);
        if (scrollerRef.current) {
          scrollerRef.current.appendChild(duplicatedItem);
        }
      });
 
      getDirection();
      getSpeed();
      setStart(true);
    }
  }
  const getDirection = () => {
    if (containerRef.current) {
      if (direction === "left") {
        containerRef.current.style.setProperty(
          "--animation-direction",
          "forwards",
        );
      } else {
        containerRef.current.style.setProperty(
          "--animation-direction",
          "reverse",
        );
      }
    }
  };
  const getSpeed = () => {
    if (containerRef.current) {
      if (speed === "superfast") {
  containerRef.current.style.setProperty("--animation-duration", "5s");
} else if (speed === "fast") {
  containerRef.current.style.setProperty("--animation-duration", "35s");
} else if (speed === "normal") {
  containerRef.current.style.setProperty("--animation-duration", "40s");
} else {
  containerRef.current.style.setProperty("--animation-duration", "80s");
}
    }
  };
  return (
    <div
      ref={containerRef}
      className={cn(
        "scroller relative z-20 max-w-7xl overflow-hidden [mask-image:linear-gradient(to_right,transparent,white_20%,white_80%,transparent)]",
        className,
      )}
    >
      <ul
        ref={scrollerRef}
        className={cn(
          "flex w-max min-w-full shrink-0 flex-nowrap gap-4 py-4",
          start && "animate-scroll",
          pauseOnHover && "hover:[animation-play-state:paused]",
        )}
      >
        {items.map((item, idx) => (
          <li
    key={idx}
className="relative w-[200px] h-[200px] shrink-0 rounded-xl border border-[#3f3f3f] bg-gradient-to-br from-[#2d2d2d] to-[#1a1a1a] text-white px-4 py-4 shadow-lg shadow-[#00000040] hover:border-[#5e5e5e] transition duration-300 flex flex-col items-center justify-center space-y-4"
>
            <img 
              src={item.image}
              alt={item.label}
              className="w-20 h-20 object-contain mb-1"
            />
            <p className="text-md font-bold text-center pt-5">{item.label}</p>
          </li>
        ))}
      </ul>
    </div>
  );
};
